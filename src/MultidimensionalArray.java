public class MultidimensionalArray {
    public static void main(String[] args){
        //TowDarray Dimjensional Array
        int[][] TowDarray = new int [2] [3];   
        TowDarray [0][0]=1;
        TowDarray [0][1]=2;
        TowDarray [0][2]=3;
        TowDarray [1][0]=4;
        TowDarray [1][1]=5;
        TowDarray [1][2]=6; 
        System.out.println(TowDarray[1][1]);
    }
}
